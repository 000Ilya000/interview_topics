//! 1. Map ‚Äì —Å–ª–æ–≤–∞—Ä—å (–∫–ª—é—á-–∑–Ω–∞—á–µ–Ω–∏–µ)

// –û—Å–Ω–æ–≤–Ω—ã–µ –º–µ—Ç–æ–¥—ã:
// ‚úî set(key, value) ‚Äì –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –ø–æ –∫–ª—é—á—É
// ‚úî get(key) ‚Äì –ø–æ–ª—É—á–∞–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –ø–æ –∫–ª—é—á—É
// ‚úî has(key) ‚Äì –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –∫–ª—é—á–∞
// ‚úî delete(key) ‚Äì —É–¥–∞–ª—è–µ—Ç —ç–ª–µ–º–µ–Ω—Ç
// ‚úî size ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤

let users = new Map();

users.set("Alice", 25);
users.set("Bob", 30);
users.set({ name: "Charlie" }, 40);

console.log(users.get("Alice")); // 25
console.log(users.has("Bob")); // true
console.log(users.size); // 3

//! 2. Set ‚Äì –∫–æ–ª–ª–µ–∫—Ü–∏—è —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π

// üìå –•—Ä–∞–Ω–∏—Ç —Ç–æ–ª—å–∫–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã, –±–µ–∑ –¥—É–±–ª–∏–∫–∞—Ç–æ–≤.

// –û—Å–Ω–æ–≤–Ω—ã–µ –º–µ—Ç–æ–¥—ã:
// ‚úî add(value) ‚Äì –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ
// ‚úî has(value) ‚Äì –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ
// ‚úî delete(value) ‚Äì —É–¥–∞–ª—è–µ—Ç
// ‚úî size ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤

let uniqueNumbers = new Set([1, 2, 2, 3, 4, 4]);

console.log(uniqueNumbers); // Set(4) {1, 2, 3, 4}

uniqueNumbers.add(5);
console.log(uniqueNumbers.has(3)); // true

uniqueNumbers.delete(2);
console.log(uniqueNumbers); // Set(4) {1, 3, 4, 5}

//! 3. WeakMap ‚Äì —Å–ª–∞–±–∞—è –∫–æ–ª–ª–µ–∫—Ü–∏—è –∫–ª—é—á-–∑–Ω–∞—á–µ–Ω–∏–µ

// üìå –†–∞–±–æ—Ç–∞–µ—Ç –∫–∞–∫ Map, –Ω–æ:
