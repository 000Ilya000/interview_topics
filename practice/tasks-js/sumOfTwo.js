const sumOfTwo = (arr, target) => {
    const seen = new Set(); // создаем множество для хранения уже просмотренных чисел

    // перебираем каждый элемент массива
    for (let num of arr) {
        let complement = target - num; // считаем, какое число должно быть в паре с текущим, чтобы в сумме получить target

        if (seen.has(complement)) {
            // если нужное "дополнение" уже есть в множестве — значит, пара найдена
            return [complement, num]; // возвращаем найденную пару чисел
        }

        seen.add(num); // если пара не найдена, сохраняем текущее число в множество для последующей проверки
        console.log(seen);
    }

    return "нет таких чисел"; // если цикл завершился и пара не найдена — возвращаем сообщение
};

console.log(sumOfTwo([1, 8, 9, 10, 21], 9)); // [1, 8]
console.log(sumOfTwo([1, 8, 9, 10, 21], 13)); // 'нет таких чисел'
