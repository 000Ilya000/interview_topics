const input = [4, 1, 5, 1, 4, 5, 2];

const singleNumber = (nums) => {
    // Используем метод reduce для свёртки массива в одно число
    // acc — аккумулятор, хранит промежуточный результат XOR
    // num — текущее число из массива
    // XOR (^) помогает "сократить" повторяющиеся числа:
    //     a ^ a = 0 (любое число с самим собой даёт 0)
    //     0 ^ a = a (0 не влияет на результат)
    // Все дублирующиеся числа сократятся, останется только уникальное
    return nums.reduce((acc, num) => acc ^ num, 0);
};

console.log(singleNumber(input));
